from math import pi

r = float(input("Enter the radius of sphere: "))
volume = (4 / 3) * pi * pow(r, 3)
print("The volume of sphere of the sphere with radius " + str(r) + " is: " + str(volume))
print("The volume of sphere of the sphere with radius " + str(r) + " is: " + str((4 / 3) * pi * r ** 3))
